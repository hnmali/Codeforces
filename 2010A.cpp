using namespace std;

void solve() {
    int n, ans = 0;
    cin >> n;
    int temp;
    for(int i = 0; i < n; i++) {
        cin >> temp;
        if(i&1)
            ans -= temp;
        else
            ans += temp;
    }
    cout << ans << '\n';
    return;
}

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);
    cout.tie(0);
    int t;
    cin >> t;
    // t = 1;
    while (t--) {
        solve();
    }
    return 0;
}
